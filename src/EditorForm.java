    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        String content = jTextArea1.getText();
        String srch = jTextField3.getText();
        String repText = jTextField4.getText();
        
        if(!jCheckBox2.isSelected()){
            content = content.toLowerCase();
            srch = srch.toLowerCase();
        }
        int pos;
        while(content.contains(srch)){
            pos = content.indexOf(srch);
            
            if(pos != -1){
                int repEnd = pos + srch.length();
                jTextArea1.replaceRange(repText, pos, repEnd);
                content = jTextArea1.getText();
            }else
                break;
        }
    }//GEN-LAST:event_jButton7ActionPerformed

