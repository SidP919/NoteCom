    public void showDialogAtFrameClosing(int flag){
        String content = jTextArea1.getText();
        String title = getTitle();
        JOptionPane pane = new JOptionPane();
        if(!content.isEmpty() && title.equals("Untitled - NoteCom")){
            
            int opt = pane.showOptionDialog(this, 
                    "Do you want to save changes made to "+title+"?", 
                    "NoteCom",
                    JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.ERROR_MESSAGE,
                    null, null, null);
            if(opt == 2){
                pane.setVisible(false);
            }else
            if(opt == 0){
                saveFile(flag);
            }else if(opt == 1){
                if(flag == 1){
                    jTextArea1.setText("");
                }
                else if(flag == 2){
                    System.exit(0);
                }
                else if(flag == 3){
                    openFile();
                }
            }
        }else{
            int opt = -1;
            if(!(title.equals("Untitled - NoteCom") && content.isEmpty()))
                opt = pane.showOptionDialog(this, 
                    "Do you want to save changes made to "+title+"?", 
                    "NoteCom",
                    JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.ERROR_MESSAGE,
                    null, null, null);
            if(opt == 2){
                pane.setVisible(false);
            }else
            if(flag == 1){
                if(opt == 0){
                    saveFile(1);
                }else if(opt == 1){
                    jTextArea1.setText("");
                }
                setTitle("Untitled - NoteCom");  
            }else if(flag == 2){
                if(opt == 0){
                    saveFile(2);
                }else if(opt == 1 || opt == -1){
                    System.exit(0);
                }
            }else if(flag == 3){
                if(opt == 0){
                    saveFile(3);
                }else if(opt == 1 || opt == -1){
                    openFile();
                }
            }
        }
    }
    
    public int showFileChooser(int i){
        if(i == 1)
            return jFileChooser1.showOpenDialog(this);
        else if(i == 2)
            return jFileChooser1.showSaveDialog(this);
        return -1;
    }
    
    public void saveFile(int flag){
        jTextArea2.setText("");
        jTextArea3.setText("");
        int opt = showFileChooser(2);
        if (opt == 0) {
            String content = jTextArea1.getText();
            f = jFileChooser1.getSelectedFile();
            try (FileOutputStream fout = new FileOutputStream(f)) {
                fout.write(content.getBytes());
            } catch (IOException e) {
                e.printStackTrace();
            }
            if (flag == 1) {
                jTextArea1.setText("");
            } else if (flag == 2) {
                System.exit(0);
            } else if (flag == 3) {
                openFile();
            } else if (flag == 4) {
                setTitle(f.getName() + " - NoteCom");
            }
        }
    }
    
    public void openFile(){
        jTextArea2.setText("");
        jTextArea3.setText("");
        int opt = showFileChooser(1);
        if(opt == 0){
            f = jFileChooser1.getSelectedFile();
            try{
                FileInputStream fin = new FileInputStream(f);
                byte b[] = new byte[fin.available()];
                fin.read(b);
                fin.close();
                String s = new String(b);
                jTextArea1.setText(s);
                setTitle(f.getName()+" - NoteCom");
            }catch(IOException e){
                e.printStackTrace();
            }
        }
    }
