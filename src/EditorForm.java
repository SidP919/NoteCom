    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int lineNumber = 1;
        try{
            lineNumber = Integer.parseInt(jTextField2.getText());
        }catch(Exception e){
            e.getMessage();
        }
        if(lineNumber >= 0){
            if (lineNumber < jTextArea1.getLineCount()) {
                if (!jCheckBoxMenuItem1.isSelected()) {
                    if (jCheckBoxMenuItem2.isSelected()) {
                        try {
                            jTextArea1.setCaretPosition(
                                jTextArea1.getLineStartOffset(lineNumber - 1));
                                jDialog2.setVisible(false);
                        } catch (BadLocationException ex) {
                            ex.getMessage();
                        }
                    } else {
                        JOptionPane.showMessageDialog(jDialog2,
                           "Cannot use \"go_to\" when Status Bar is disabled!");
                    }
                } else {
                    JOptionPane.showMessageDialog(jDialog2,
                            "Cannot use \"go_to\" when Word_Wrap is enabled!");
                }
            }else{
                JOptionPane.showMessageDialog(jDialog2, 
                        "This line number doesn't exist!");
            }
        }else{
            JOptionPane.showMessageDialog(jDialog2,
                    "please enter a positive number.");
        }   
    }//GEN-LAST:event_jButton3ActionPerformed

